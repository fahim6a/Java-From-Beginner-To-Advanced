public class Resulting_data_type {
    public static void main(String[] args) {
         // int to int data type
        int a= 44+56;
        System.out.println(a);
        // byte and integer variables result integer
        byte a1= 45;
        int b1= a1+12;
        System.out.println(b1);
        //short and integer variables result integer
        short c=45;
        int d= c+12;
        System.out.println(d);
        //short and byte result int
        short c1=45;
        byte d1=12;
        int sum= c1 + d1;
        System.out.println(sum);
        // long + float = float
        long x=45;
        float xx=12.2f;
        float xxx= x+xx;
        System.out.println(xxx);
        //long + double= double
        long x1= 45;
        double x2 = 45.0;
        double sum1= x1+x2;
        System.out.println(sum1);

        // float + double = double
        float w = 45;
        double e=45.0;
        double s= w+e;
        System.out.println(s);

        //char + int = int
        char ch= 78;
        int ci= 45;
        System.out.println(ch+ci);
        //char + short = int
        char ch1=78;
        short ch2=2;
        System.out.println(ch1+ch2);


    }
}
