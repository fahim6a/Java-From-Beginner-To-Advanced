public class Foreach_loop {
    public static void main(String[] args) {
        //what is foreach loop
        //foreach loop is s simplified version of an for loop
        int[] england ={44,55,444,888,48,4578,45488};
        for (int elements: england) {
            System.out.println(elements);
        }
        //i values doesn't available in for-each loop
        //that means only elements can display.

    }
}
