public class program_literals {
    public static void main(String[] args){
        //what is literals?
        //literals is a constant value which can be assigned to the variable
        //is called literals
        //example
        byte age=22;//here byte is a data type and 22 is a value
        //22 value contain age. age is a variables name
        System.out.println(age);
        char latter ='a';// string literals
        System.out.println(latter);
        float number=99.98f;// here you might notice i've assigned an extra f
        //right to the end of 99.98. floating and double datatype used to contain
        // fractional data where double is a by default data type for contain
        // fractional data. we don't have to used d right after the fractional number
        System.out.println(number);
        double number1=99.98;//look we are not using 'd' after the 99.98.
        System.out.println(number1);

    }
}
